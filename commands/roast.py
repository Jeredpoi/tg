# ==============================================================================
# commands/roast.py — Команда /roast
# ==============================================================================

import random
from telegram import Update
from telegram.ext import ContextTypes

ROAST_PHRASES = [
    "{user} сегодня главный клоун чата 🤡",
    "{user} уровень интеллекта: Wi-Fi без интернета 📶",
    "{user} опять что-то написал и опять мимо 😬",
    "{user} — живое доказательство что эволюция иногда даёт сбой 🦧",
    "{user} написал что-то настолько умное, что никто не понял. Особенно он сам 🧠❌",
    "{user} был бы полезен... если бы чат был про молчание 🤐",
    "{user} — тот человек, которого Google предлагает забыть 🔍",
    "{user} мог бы быть хуже. Мог бы. Но не старается 😐",
    "{user} — редкое сочетание уверенности и некомпетентности 🏆",
    "{user} говорит так уверенно, что даже его ошибки звучат как приговор 😤",
    "{user} — единственный человек, который способен спорить с калькулятором и проиграть 🔢",
    "{user} настолько уникален, что даже спам-фильтр удалил его из жизни 📧",
    "{user} если бы молчание было золотом — ты бы всё равно был в долгах 💸",
    "{user} твои сообщения настолько редкие, что их уже занесли в Красную книгу 📕",
    "{user} консультировался с умными людьми. Они попросили его уйти 🚪",
    "{user} — загадка природы: как при таком IQ ещё работает клавиатура ⌨️",
    "{user} думает что троллит, но на самом деле просто... такой 🐟",
    "{user} — офлайн-версия 404: мозг не найден 🖥️",
    "{user} если бы глупость была спортом — ты бы выступал на Олимпийских играх 🥇",
    "{user} даже твой кот стесняется рассказывать что живёт с тобой 🐱",
]


async def roast_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Обработчик команды /roast @username."""
    if not context.args:
        await update.message.reply_text("ℹ️ Укажи цель!\nПример: /roast @username")
        return

    target = context.args[0]
    if not target.startswith("@"):
        target = f"@{target}"

    phrase = random.choice(ROAST_PHRASES).format(user=target)
    await update.message.reply_text(phrase)
